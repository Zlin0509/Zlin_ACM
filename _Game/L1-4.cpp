inline void Zlin() {
    int n;
    cin >> n;
    vector<db> a(n);
    db sum = 0;
    for(auto &it: a) {
        cin >> it;
        sum += 1 / it;
    }
    sum /= n;
    sum = 1.0 / sum;
    printf("%.02lf", sum);
}

